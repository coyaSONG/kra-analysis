def canonical_field_metadata_rows() -> tuple[dict[str, str], ...]:
    """정규 CSV에 기록할 메타데이터 규칙 행들을 반환한다."""

    return tuple(rule.to_csv_row() for rule in FIELD_METADATA_RULES)


def match_field_metadata_rule(path: str) -> FieldMetadataRule | None:
    """런타임 payload 경로에 대응하는 중앙 메타데이터 규칙을 찾는다."""

    normalized_path = normalize_field_path(path)
    leaf_key = normalized_path.rsplit(".", 1)[-1]

    for rule in FIELD_METADATA_RULES:
        if rule.match_type != "exact":
            continue
        if normalized_path in rule.normalized_patterns():
            return rule

    for rule in FIELD_METADATA_RULES:
        if rule.match_type != "prefix":
            continue
        for pattern in rule.normalized_patterns():
            if (
                normalized_path == pattern
                or normalized_path.startswith(f"{pattern}.")
                or normalized_path.startswith(f"{pattern}[")
            ):
                return rule

    for rule in FIELD_METADATA_RULES:
        if rule.match_type != "leaf":
            continue
        if leaf_key in rule.normalized_patterns():
            return rule

    return None
