@dataclass(frozen=True, slots=True)
class FieldMetadataRule:
    """출전표 확정 시점 필드 메타데이터 규칙 한 행.

    `field_path` 는 CSV/문서 기준 canonical path 이다.
    `match_type` 은 런타임 payload 경로를 어떤 방식으로 이 규칙에 매칭할지 정한다.
    """

    field_path: str
    field_role: str
    source_api: str
    source_field: str
    availability_stage: str
    publication_basis: str
    publication_basis_refs: tuple[str, ...]
    exception_rule: str
    validation_status: str
    validation_evidence: tuple[str, ...]
    operational_status: str
    train_inference_flag: str
    match_type: str = "exact"
    path_aliases: tuple[str, ...] = ()
    join_key: str = ""
    value_type: str = ""
    mutable_after_publish: str = ""
    owner: str = DEFAULT_RULE_OWNER
    last_verified_at: str = ""
    notes: str = ""

    def __post_init__(self) -> None:
        if self.field_role not in FIELD_ROLES:
            raise ValueError(f"unsupported field_role: {self.field_role}")
        if self.availability_stage not in AVAILABILITY_STAGES:
            raise ValueError(
                f"unsupported availability_stage: {self.availability_stage}"
            )
        if self.validation_status not in VALIDATION_STATUSES:
            raise ValueError(f"unsupported validation_status: {self.validation_status}")
        if self.operational_status not in OPERATIONAL_STATUSES:
            raise ValueError(
                f"unsupported operational_status: {self.operational_status}"
            )
        if self.train_inference_flag not in TRAIN_INFERENCE_FLAGS:
            raise ValueError(
                f"unsupported train_inference_flag: {self.train_inference_flag}"
            )
        if self.match_type not in RULE_MATCH_TYPES:
            raise ValueError(f"unsupported match_type: {self.match_type}")

    def normalized_patterns(self) -> tuple[str, ...]:
        return tuple(
            normalize_field_path(path) for path in (self.field_path, *self.path_aliases)
        )

    def to_csv_row(self) -> dict[str, str]:
        return {
            "metadata_schema_version": METADATA_SCHEMA_VERSION,
            "field_path": self.field_path,
            "field_role": self.field_role,
            "source_api": self.source_api,
            "source_field": self.source_field,
            "availability_stage": self.availability_stage,
            "publication_basis": self.publication_basis,
            "publication_basis_refs": _encode_multi_value(self.publication_basis_refs),
            "exception_rule": self.exception_rule,
            "validation_status": self.validation_status,
            "validation_evidence": _encode_multi_value(self.validation_evidence),
            "operational_status": self.operational_status,
            "train_inference_flag": self.train_inference_flag,
            "join_key": self.join_key,
            "value_type": self.value_type,
            "mutable_after_publish": self.mutable_after_publish,
            "owner": self.owner,
            "last_verified_at": self.last_verified_at,
            "notes": self.notes,
        }


def normalize_field_path(path: str) -> str:
    """배열 인덱스를 제거한 canonical field path를 반환한다."""

    return _PATH_INDEX_PATTERN.sub("[]", path)


def _encode_multi_value(values: tuple[str, ...]) -> str:
    return json.dumps(list(values), ensure_ascii=False)


